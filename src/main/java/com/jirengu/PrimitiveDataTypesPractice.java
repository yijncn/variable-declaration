package com.jirengu;

public class PrimitiveDataTypesPractice {
    public static void main(String[] args) {
        // 先定义变量，然后再修改对应的打印语句按照README中的格式打印变量

        //定义变量
        byte age = 10;
        // 修改语句打印变量
        System.out.println("Age is: " + age);
        
        //定义变量
        short height = 30000;
        // 修改语句打印变量
        System.out.println("Height is: " + height);

        //定义变量
        int num = 1000000000;
        // 修改语句打印变量
        System.out.println("Num is: " + num);

        //定义变量
        long distance = 10000000000L;
        // 修改语句打印变量
        System.out.println("Distance is: " + distance);

        //定义变量
        float rate = 6/5f;
        // 修改语句打印变量
        System.out.println("Rate is: " + rate);

        //定义变量
        double fixedRate = 456/123d;
        // 修改语句打印变量
        System.out.println("Fixed rate is: " + fixedRate);

        //定义变量
        char ch = 'A';
        // 修改语句打印变量
        System.out.println("Ch: " + ch);

        //定义变量
        boolean isUpdate = true;
        // 修改语句打印变量
        System.out.println("isUpdated: " + isUpdate);
        
    }
}
